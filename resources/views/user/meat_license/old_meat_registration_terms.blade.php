    					</li>
    					<li class="d-flex pb-20">
    					    २६. ज्याला संसर्गजन्य रोग झाला आहे किंवा ज्यांना एखादी जखम झाली आहे ,ज्यामुळे तो कोणत्याही पदार्थाला स्पर्श करू शकत नाही अशा व्यक्तीस परवानाधारक नोकरीस ठेवणार नाही ,अशा प्रकारचा एखादा माणूस जर काम करत असताना आढळल्यास त्याला कामावरून काढून टाकेपर्यंत परवाना रद्द केले जाईल.
    					</li>
    					<li class="d-flex pb-20">
    					    २७. चोरीमुळे,आग लागल्यास ,आणि पाणी साठवल्यामुळे,दिरंगाई किंवा कोणत्याही आकस्मात किंवा कोणत्याही कारणामुळे परवानाधारकांचे नुकसान झाल्यास त्यास महापालिका आयुक्त जबाबदार असणार नाहीत.
    					</li>
