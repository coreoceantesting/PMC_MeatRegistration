    					    १९. परवानाधारकाने सर्व वेळी महापालिका आयुक्तांना आणि पशुधन विकास अधिकारी किंवा कोणत्याही महापालिका अधिकाऱ्यास किंवा त्यांना अधिकाऱ्यास किंवा त्यांना अधिकृत केलेल्या कर्मचाऱ्यास कि जे कर्मचारी महापालिका आयुक्तांच्या नियंत्रणाखाली असतील अशा कर्मचार्यांना प्रोसेसिंग युनिटमध्ये केव्हाही येण्यास प्रवेश करण्यास तपासणी करण्यास आणि प्रोसेसिंग प्रक्रियेचे पर्यवेशण करण्याची मुभा दिली पाहिजे.
						</li>
    					<li class="d-flex pb-20">
						    २०. महापालिकेच्या प्रशिक्षित कर्मचाऱ्यांना मांसाच्या प्रोसेसिंग प्रक्रियेची दररोज पर्यवेक्षण करण्याची मुभा परवानाधारक देईल,आणि त्याची प्रोसेसिंग युनिटमध्येच बसण्याची व्यवस्था करून त्यांना योग्य ते सहकार्य दिले जाईल.मांसाच्या गुणवत्तेबद्दल त्यांनी दिलेल्या सूचनांचे पालन केले जाईल आणि अशा कर्मचाऱ्यांनी  खाण्यास अयोग्य असल्याचे जाहीर केल्यास असा माल नष्ट केला जाईल.
						</li>
    					<li class="d-flex pb-20">
						    २१.बीफ आणि मांस यासाठी स्वतंत्र प्रोसेसिंग विभाग वापरले जातील आणि फ्रोझन मांस व्यवस्थेत पॅक केल्यावर आणि त्यावर मांस बीफ याबाबतीत मार्क केल्यानंतर तसेच पॅक केलेले पॅकेट्स निर्यातीसाठी कि स्थानिक वापरासाठी आहेत,याबाबतीत स्वतंत्र मार्क केल्यानंतर अशी फ्रोझन माणसाची विक्री केली जाईल किंवा विक्रीसाठी पुरवठा केला जाईल.
						</li>
    					<li class="d-flex pb-20">
						    २२. हे परवानापत्र हस्तांतरणीय नाही,परवानाधारक,परवानापत्र,दिलेली जागा ही त्यांच्या व्यवसायासाठीच फक्त (स्वतः धंदा करण्यासाठी )किंवा त्यांच्या प्रत्यक्ष पर्यवेक्षणाखाली आणि नियंत्रणाखाली व्यवसायासाठी वापरली जाईल.
						</li>
    					<li class="d-flex pb-20">
						    २३. परवानापत्रधारक वरील ठिकाणी स्वतः धंदा करेल किंवा त्यांच्या तात्पुरत्या गैरहजेरीत त्यांचे व्यवसायासाठीच फक्त (स्वतः धंदा करण्यासाठी) किंवा त्यांच्या पर्यवेक्षणाखाली आणि नियंत्रणाखाली व्यवसायासाठीच वापरली जाईल.
						</li>
