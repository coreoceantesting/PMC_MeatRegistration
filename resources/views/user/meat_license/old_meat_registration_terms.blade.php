    					</li>
    					<li class="d-flex pb-20">
    					    ३०. अशा प्रकारे परवानापत्र रद्द झाल्यानंतर , संपल्यानंतर किंवा स्थगित झाल्यानंतर अशा परवानाधारकांस मांसाचा पुरवठा करता येणार नाही.
